/* ══════════════════════════════════════════════
   I18N ENGINE
══════════════════════════════════════════════ */

function getLang() {
  const urlParam = new URLSearchParams(window.location.search).get('lang');
  if (urlParam && TRANSLATIONS[urlParam]) {
    localStorage.setItem('sv_lang', urlParam);
    return urlParam;
  }
  return localStorage.getItem('sv_lang') || 'nl';
}

function setLang(lang) {
  localStorage.setItem('sv_lang', lang);
  applyI18n(lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });
  if (window.MiralCart) window.MiralCart.renderSidebar();
}

function t(key) {
  const lang = getLang();
  const parts = key.split('.');
  let obj = TRANSLATIONS[lang] || TRANSLATIONS.nl;
  for (const p of parts) { obj = obj?.[p]; if (obj === undefined) break; }
  if (obj === undefined) {
    let fallback = TRANSLATIONS.nl;
    for (const p of parts) { fallback = fallback?.[p]; if (fallback === undefined) break; }
    return fallback ?? key;
  }
  return obj;
}

function applyI18n(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (val !== undefined && val !== key) el.innerHTML = String(val).replace(/\n/g,'<br>');
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const val = t(el.dataset.i18nPh);
    if (val && val !== el.dataset.i18nPh) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const val = t(el.dataset.i18nTitle);
    if (val && val !== el.dataset.i18nTitle) document.title = val + ' – Spanvloeren.nl';
  });
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });
}
